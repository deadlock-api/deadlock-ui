/**
 * Fetches all item class names from the Deadlock API and generates:
 * 1. TypeScript union type for autocomplete (packages/core/src/types/item-class-name.ts)
 * 2. JSON catalog for docs (docs/src/data/items.json)
 *
 * Usage: node scripts/generate-class-names.mjs
 */

const API_URL = 'https://assets.deadlock-api.com/v2/items?language=english';
const TYPE_OUTPUT = new URL('../packages/core/src/types/item-class-name.ts', import.meta.url);
const JSON_OUTPUT = new URL('../docs/src/data/items.json', import.meta.url);

const res = await fetch(API_URL);
if (!res.ok) throw new Error(`API request failed: ${res.status}`);

const allItems = await res.json();
const items = allItems.filter((i) => i.type === 'upgrade' && i.shopable);

// --- 1. TypeScript union type ---
const classNames = items.map((i) => i.class_name).sort();

const tsLines = [
  '// Auto-generated by scripts/generate-class-names.mjs — do not edit manually.',
  '',
  'export type ItemClassName =',
  classNames.map((n) => `  | '${n}'`).join('\n') + ';',
  '',
];

// --- 2. JSON catalog for docs ---
const catalog = items
  .map((i) => ({
    class_name: i.class_name,
    name: i.name,
    type: i.type,
    item_slot_type: i.item_slot_type,
    item_tier: i.item_tier,
    image: i.shop_image_webp || i.shop_image || i.image_webp || i.image || null,
  }))
  .sort((a, b) => a.class_name.localeCompare(b.class_name));

const { writeFileSync, mkdirSync } = await import('node:fs');
const { fileURLToPath } = await import('node:url');
const { dirname } = await import('node:path');

writeFileSync(fileURLToPath(TYPE_OUTPUT), tsLines.join('\n'));

const jsonPath = fileURLToPath(JSON_OUTPUT);
mkdirSync(dirname(jsonPath), { recursive: true });
writeFileSync(jsonPath, JSON.stringify(catalog, null, 2) + '\n');

console.log(`Generated ${classNames.length} class names → packages/core/src/types/item-class-name.ts`);
console.log(`Generated ${catalog.length} items catalog  → docs/src/data/items.json`);
